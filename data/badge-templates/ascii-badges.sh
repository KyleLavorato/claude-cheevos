#!/usr/bin/env bash
# ascii-badges.sh - ASCII art badge templates for terminal display
# Used as fallback when SVG/image display is not available

show_badge() {
    local tier="$1"

    case "$tier" in
        beginner)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘   в—ўв–Ҳв—Ј     в•‘
    в•‘  в—ўв–Ҳв–Ҳв–Ҳв—Ј    в•‘
    в•‘ в—ўв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв—Ј   в•‘
    в•‘ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳ   в•‘
    в•‘  в—Ҙв–Ҳв–Ҳв–Ҳв—Ө    в•‘
    в•‘   вӯҗпёҸ      в•‘
    в•‘ BEGINNER  в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        intermediate)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘      вҳ…        в•‘
    в•‘    вҳ… вҳ… вҳ…      в•‘
    в•‘  вҳ…   в—ү   вҳ…    в•‘
    в•‘    вҳ… вҳ… вҳ…      в•‘
    в•‘      вҳ…        в•‘
    в•‘               в•‘
    в•‘ INTERMEDIATE  в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        experienced)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘   вҺ§ в—ү вҺ«       в•‘
    в•‘   в”Ӯв–“в–“в–“в”Ӯ       в•‘
    в•‘   в”Ӯв–“в–“в–“в”Ӯ       в•‘
    в•‘   в”Ӯв–“в–“в–“в”Ӯ       в•‘
    в•‘  в•ұв–”в–”в–”в–”в–”в•І      в•‘
    в•‘ в•ұ   вӯҗпёҸ   в•І    в•‘
    в•‘ EXPERIENCED   в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        master)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘  вҷҰ вҷӣ вҷҰ        в•‘
    в•‘ в—ўв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв—Ј     в•‘
    в•‘ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳ     в•‘
    в•‘ в—Ҙв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв—Ө     в•‘
    в•‘  в•ұв–”в–”в–”в–”в–”в•І      в•‘
    в•‘ в”Ӯ вҳ… вҳ… вҳ… в”Ӯ     в•‘
    в•‘   MASTER      в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        impossible)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘     в—Ҡ         в•‘
    в•‘    в—Ҡв—Ҡв—Ҡ        в•‘
    в•‘   в—Ҡв—Ҡв—Ҡв—Ҡв—Ҡ       в•‘
    в•‘  в—Ҡв—Ҡв—Ҡв—Ҡв—Ҡв—Ҡв—Ҡ      в•‘
    в•‘   в—Ҡв—Ҡв—Ҡв—Ҡв—Ҡ       в•‘
    в•‘    в—Ҡв—Ҡв—Ҡ    вңЁ  в•‘
    в•‘  IMPOSSIBLE   в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        secret)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘  в”Ҹв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”“    в•‘
    в•‘  в”ғ ????? в”ғ    в•‘
    в•‘  в”ғ   ?   в”ғ    в•‘
    в•‘  в”ғ ????? в”ғ    в•‘
    в•‘  в”—в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”ӣ    в•‘
    в•‘      рҹҺҒ        в•‘
    в•‘    SECRET     в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        rank)
            cat << 'EOF'
    в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—
    в•‘   рҹ‘‘ RANK рҹ‘‘   в•‘
    в•‘               в•‘
    в•‘   в•”в•җв•җв•җв•җв•җв•җв•җв•—   в•‘
    в•‘   в•‘ вҳ… вҳ… вҳ… в•‘   в•‘
    в•‘   в•‘  вҳ…вҳ…вҳ…  в•‘   в•‘
    в•‘   в•ҡв•җв•җв•җв•җв•җв•җв•җв•қ   в•‘
    в•‘               в•‘
    в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ
EOF
            ;;
        *)
            echo "Unknown tier: $tier"
            return 1
            ;;
    esac
}

# If called directly, show the requested badge
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    if [[ $# -lt 1 ]]; then
        echo "Usage: $0 <tier>"
        echo "Tiers: beginner, intermediate, experienced, master, impossible, secret, rank"
        exit 1
    fi
    show_badge "$1"
fi
